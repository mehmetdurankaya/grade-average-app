package dersortalamasiapp;

public class ArithmeticMean {

    /*   1- Girilen üç sayının ortalamasını ekrana yazdırınız
         2- Kullanicidan aldiginiz 3 basamakli bir sayinin rakamlari toplamini bulunuz
         ornegin => 312 icin 3+1+2=6
    */
       public int ortalamaHesapla(int bir,int iki,int uc,int dort,int bes,int alti){
        return (bir+iki+uc+dort+bes+alti)/6;

    }


}
